#ifndef CAF_POLICY_ARG_HPP
#define CAF_POLICY_ARG_HPP

namespace caf {
namespace policy {

/// Provides a wrapper to pass policy types as values to functions.
template <class... Ts>
struct arg {
public:
  static const arg value;
};

template <class... Ts>
const arg<Ts...> arg<Ts...>::value = arg<Ts...>{};

} // namespace policy
} // namespace caf

#endif // CAF_POLICY_ARG_HPP
