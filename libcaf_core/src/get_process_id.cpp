#include "caf/detail/get_process_id.hpp"

#include "caf/config.hpp"

 #ifdef CAF_WINDOWS
#  include <Windows.h>
#else
#  include <sys/types.h>
#  include <unistd.h>
#endif

namespace caf {
namespace detail {

unsigned get_process_id() {
# ifdef CAF_WINDOWS
  return GetCurrentProcessId();
# else
  return static_cast<unsigned>(getpid());
# endif
}

} // namespace detail
} // namespace caf
